package com.example.database_project_salesman.SKU;

public class SkuCompany
{
    String id, name;

    public SkuCompany()
    {
    }

    public SkuCompany(String id, String name)
    {
        this.id = id;
        this.name = name;
    }

    public String getId()
    {
        return id;
    }

    public void setId(String id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    @Override
    public String toString()
    {
        return name;
    }
}
